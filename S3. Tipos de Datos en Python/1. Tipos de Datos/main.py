# Data Types
# Numericos (Integers, Complex Number, Float)
# Dictionary
# Boolean
# Set
# Sequence Type (Strings, List, Tuple)

x: int = 10
print(x)
print(type(x))

y: float = 17.50  # Con el : le damos una indicacion del tipo de dato, no quiere decir que si o si sea ese tipo.
print(y)
print(type(y))

b: bool = True  # Con los boolean se respetan las mayusculas
print(b)
print(type(b))

s: str = "String"  # Puedo utilizar comillas simples
print(s)
print(type(s))